app
  .route('/api/v1/tours')
  .get(getAllTours)
  .get(getTour)
  .post(createTour)
  .patch(updateTour)
  .delete(deleteTour);

const port = 3000;
app.listen(port, () => {
  console.log(`App running on port ${port}...`);
});
